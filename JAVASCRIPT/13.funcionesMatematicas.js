//FUNCINES MÁTEMATICAS  

//valor absoluto de un número(valor sin signo) 
console.log(Math.abs(-56)) 
//potencias 
//pow(base,exponente)
console.log(Math.pow(2,2)) 
//Raiz cuadrada de un número 
//sqrt(num)
console.log(Math.round(4.40)) 
//Math.ceil(numero)
console.log(Math.ceil(6.01)) 
//Redondea hacia abajo 
console.log(Math.floor(6.999999))
//Numero Aleatorio 
console.log(Math.round(Math.random()*255)) 
console.log(Math.round(Math.random()*(100-20)+20)) 
//Raiz cuadrada de un numero 
//squrt(num)
console.log(Math.sqrt(4))
//Sign(nos dice el signo de el numero)
console.log(Math.sign(-45))
